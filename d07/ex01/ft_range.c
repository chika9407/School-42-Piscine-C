#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int i;
	int *range;

	range = (int*)malloc(sizeof(int) * (max - min));
	i = 0;
	while (min < max)
	{
		range[i] = min;
		i++;
		min++;
	}
	return (range);
}
